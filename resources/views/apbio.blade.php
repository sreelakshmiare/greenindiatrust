                    <h4 class="text-justify text-success">Objectives of PBR :</h4>
                    <p class="text-justify text-success">The People's Biodiversity Register (PBR) seeks to document the knowledge of occurrence, practices of propagation, sustainable harvests and conservation, as well as economic uses of biodiversity resources that reside with India's local communities. The objectives of the programme would thus embrace the creation of decentralized country wide databases on
					</p>
						
					 <ol class = "orderlist text-success">
						 <li class="text-justify text-success">A Status of biodiversity resources such as populations of medicinal plants, cultivars of fruit trees or freshwater fishes.</li>
						 <li class="text-justify text-success">A Various factors such as harvests from natural populations, changes in agricultural practices or discharge of industrial effluents, affecting the biodiversity resources. </li>
						 <li class="text-justify text-success">An Ongoing involvement of local communities /individuals in sustainable use and conservation of biodiversity resources, such as systems of regulated grazing on pastures, maintenance of varieties of fruit plants like jackfruit on farm bunds or protection of fish in sacred ponds.</li>
						 <li class="text-justify text-success">A Local knowledge, widely shared and publicly disclosed, of properties and uses of biodiversity resources e.g. drought-resistance of a certain cultivar, methods of preservation of food or use of certain plants in treating human or livestock diseases. Local knowledge only partially disclosed; for instance, a claim that a particular medicine woman knows of a cure for asthma. </li>
						 <li class="text-justify text-success">A Validate the information thus recorded for instance, through cross-checking with other published information sources. </li>
						 <li class="text-justify text-success">A Create a network of decentralized databases, ultimately linking them to a consolidated national Data base which would give full credit to the origin of information at the level of an individual, a community or a village panchayat.</li>
						 <li class="text-justify text-success">Feed pertinent information, such as volume of trade and prices of medicinal plants back to the local communities. 
						</li>
				</ol>

				<h4 class="text-justify text-success">The methodology of PBR documentation involved :</h4>

						<ol class = "orderlist text-success">
							<li class="text-justify text-success">Pre PBR visits </li>
							<li class="text-justify text-success">Awareness meetings</li>
							<li class="text-justify text-success">Identification of Technical Support Group member</li>
							<li class="text-justify text-success">Training of field investigators </li>
							<li class="text-justify text-success">Collection of data from Gram Panchayat, Health Centre, Agriculture, Forest and other Government Departments</li>
							<li class="text-justify text-success">Consultation with BMC Members and Village leaders.</li>
							<li class="text-justify text-success">Liaison with line departments.</li>

							<li class="text-justify text-success">Consultations with Universities, Colleges and other Institution.</li>
							<li class="text-justify text-success">Identification of community health knowledge holders including healers.</li>
							<li class="text-justify text-success">Desk research for ayurveda assessment of medicinal plants.</li>
							<li class="text-justify text-success">Data compilation.</li>
							<li class="text-justify text-success">Transect walks for identification of flora.</li>
							<li class="text-justify text-success">Documentation from community health knowledge holders including healers during transect walks, focus group discussions and individual interviews.</li>
							<li class="text-justify text-success">Drafting of PBR in consultation with experts.</li>
							<li class="text-justify text-success">Focus Group Discussions for elucidating details on the linkages of biological components with nutritional, health and livelihood security and conservation concern.</li>
							<li class="text-justify text-success">Authentication of flora.</li>
							<li class="text-justify text-success">Approval by BMC, State Biodiversity Board.</li>
					</ol>
